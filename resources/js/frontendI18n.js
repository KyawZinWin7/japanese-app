function getValue(source, path) {
    return path.split('.').reduce((carry, part) => carry?.[part], source);
}

function format(message, params = {}) {
    return Object.entries(params).reduce(
        (carry, [key, value]) => carry.replaceAll(`{${key}}`, String(value)),
        message,
    );
}

export function getLocale() {
    return locale;
}

export function t(path, params = {}) {
    const message = getValue(messages[locale], path) ?? getValue(messages.en, path) ?? path;

    return typeof message === 'string' ? format(message, params) : path;
}

export function hasTranslation(path) {
    return getValue(messages[locale], path) != null || getValue(messages.en, path) != null;
}
